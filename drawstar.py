import turtle


turtle.color("blue")
turtle.pendown()
turtle.goto(100,100)
turtle.penup()
turtle.pendown()
turtle.goto(200,0)
turtle.penup()
turtle.pendown()
turtle.goto(0,0)
turtle.penup()
turtle.goto(0,50)
turtle.pendown()
turtle.goto(100,-50)
turtle.penup()
turtle.pendown()
turtle.goto(200,50)
turtle.penup()
turtle.pendown()
turtle.goto(0,50)

turtle.done()
